#Escriba un programa en Python para invertir una cadena.
#Cadena de muestra: "1234abcd"
#Salida esperada: "dcba4321"
cadena_de_muestra = input(str('Ingrese la cadena a invertir: '))
salida_esperada = ''.join(reversed(cadena_de_muestra))
print(cadena_de_muestra)
print(salida_esperada)